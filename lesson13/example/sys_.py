import sys

# 1. Получить список аргументов командной строки (включая имя скрипта)
print("1. sys.argv:", sys.argv)

# 2. Завершить программу с кодом возврата (0 — успешное завершение)
# sys.exit(0)  # Раскомментируйте для выхода из программы

# 3. Получить версию Python и различные сведения о ней
print("3. sys.version:", sys.version)
print("   sys.version_info:", sys.version_info)

# 4. Получить текущий лимит рекурсии и изменить его
print("4. Текущий лимит рекурсии:", sys.getrecursionlimit())
sys.setrecursionlimit(1500)  # установим новый лимит рекурсии
print("   Новый лимит рекурсии:", sys.getrecursionlimit())

# 5. Определить платформу, на которой запущена программа
print("5. Платформа:", sys.platform)

# 6. Получить размер объекта в байтах (полезно для анализа памяти)
a = [1, 2, 3]
print("6. Размер списка a в байтах:", sys.getsizeof(a))

# 7. Доступ к стандартным потокам ввода-вывода
print("7. Используем sys.stdout для вывода без переноса строки")
sys.stdout.write("Привет, stdout!\n")

# 8. Получить словарь загруженных модулей
print("8. Загруженные модули:", list(sys.modules.keys())[:5], "...")  # первые 5 модулей

# 9. Манипулировать путями поиска модулей
print("9. Текущий sys.path (пути для поиска модулей):")
for p in sys.path[:3]:  # покажем первые 3 пути
    print("   ", p)

